/* Q1: Write a Java program that defines the quantity and price of sugar.
   The program should then calculate and display the total sugar bill. */
public class Q1 {

    public static void main(String[] args) {
        double SugarQuantity = 5.5;
        double PricePerKg = 60.0;
        double TotalBill = SugarQuantity * PricePerKg;
        System.out.println("Total Sugar Bill = Rs. " + TotalBill + " PKR");
        System.out.println("Coded By: Partab Saathi");
    }
}
