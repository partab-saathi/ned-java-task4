/* Q3: Write a Java program that defines the amount of fuel in liters and the price per liter.
The program should calculate and display the total cost of the fuel. */

public class Q3 {

    public static void main(String[] args) {
        int FuelInLiters = 40;
        float PricePerLiter = 285.5f;
        float TotalCost = FuelInLiters * PricePerLiter;
        System.out.println("Total Cost of Fuel = Rs. " + TotalCost + " PKR");
        System.out.println("Coded By: Partab Saathi");
    }
}
